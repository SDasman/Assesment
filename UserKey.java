public class UserKey {
	 
	private String name;
	private String userid;
 
	public UserKey(String name, String userid) {
		this.name = name;
		this.userid = userid;
	}
 
	public String getName() {
		return name;
	}
 
	public String getUserID() {
		return userid;
	}
}
